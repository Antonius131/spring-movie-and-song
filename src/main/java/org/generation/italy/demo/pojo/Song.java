package org.generation.italy.demo.pojo;

public class Song {

	private int id;
	private int text;
	
	public Song() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getText() {
		return text;
	}

	public void setText(int text) {
		this.text = text;
	}
}
